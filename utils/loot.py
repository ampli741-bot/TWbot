RARITY_CHANCE = [
    ("legendary", 0.02),
    ("epic", 0.08),
    ("rare", 0.25),
    ("common", 0.65)
]

def drop_loot():
    roll = random.random()

    cumulative = 0
    for rarity, chance in RARITY_CHANCE:
        cumulative += chance
        if roll <= cumulative:
            item = random.choice(LOOT_TABLE[rarity])
            name, power = item

            # 🎯 небольшой рандом к силе
            bonus = random.randint(0, 3)
            return name, power + bonus

    return None